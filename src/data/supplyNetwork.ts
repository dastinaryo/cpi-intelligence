import type { NodeStatus, SupplyNode, SupplyRoute } from "@/types/dashboard";

const deriveStatus = (utilization: number): NodeStatus => {
  if (utilization >= 92) return "bottleneck";
  if (utilization >= 78) return "high";
  return "normal";
};

const makeNode = (raw: Omit<SupplyNode, "utilization" | "status">): SupplyNode => {
  const utilization = Math.round(
    (raw.currentThroughputTon / Math.max(raw.capacityTonPerMonth, 1)) * 100,
  );
  return { ...raw, utilization, status: deriveStatus(utilization) };
};

